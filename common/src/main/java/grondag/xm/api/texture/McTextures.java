	public static TextureSet tile(String name) {
		return TextureSet.builder()
				.displayNameToken("mc_" + name).baseTextureName("minecraft:block/" + name)
				.versionCount(1).scale(SINGLE).layout(TextureLayoutMap.SINGLE).transform(IDENTITY)
				.renderIntent(TextureRenderIntent.BASE_ONLY).groups(TextureGroup.STATIC_TILES).build("minecraft:" + name);
	}

	public static TextureSet decal(String name) {
		return TextureSet.builder()
				.displayNameToken("mc_" + name).baseTextureName("minecraft:block/" + name)
				.versionCount(1).scale(SINGLE).layout(TextureLayoutMap.SINGLE).transform(IDENTITY)
				.renderIntent(TextureRenderIntent.OVERLAY_ONLY).groups(TextureGroup.STATIC_DETAILS).build("minecraft:" + name);
	}
